package Start;

public class Bag {

	public static void main(String[] args) {

		
		for(int i=0;i<5;i++) {
			
			for(int j=i;j<=i;j++) {
				
				for(int k=i;k<=i;k++) {
					System.out.println(" ");
				}
				System.out.print("*");
			}
			
			System.out.println();
			
		}
	}

}
